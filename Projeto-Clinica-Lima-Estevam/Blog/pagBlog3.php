        <p>
        Cuidar da saúde do coração é essencial para garantir qualidade de vida e prevenir doenças graves. Muitas pessoas acreditam que só precisam procurar um cardiologista quando sentem dores intensas, mas a avaliação preventiva é fundamental para evitar complicações futuras.
        </p>

        <p>
        Um dos principais sinais de alerta é a dor no peito, que pode se manifestar como pressão, queimação ou desconforto. Esse sintoma pode estar relacionado a problemas cardíacos e deve ser investigado o quanto antes por um especialista.
        </p>

        <p>
        Falta de ar, especialmente durante esforços leves ou até mesmo em repouso, também merece atenção. Esse sintoma pode indicar alterações no funcionamento do coração ou dos pulmões, sendo essencial uma avaliação médica adequada.
        </p>

        <p>
        Outro ponto importante são as palpitações, que são sensações de batimentos cardíacos acelerados, irregulares ou muito fortes. Embora possam ocorrer por ansiedade ou estresse, também podem estar associadas a arritmias cardíacas.
        </p>

        <p>
        Tonturas, desmaios e cansaço excessivo sem motivo aparente são sinais que não devem ser ignorados. Esses sintomas podem indicar que o coração não está conseguindo bombear sangue de forma eficiente para o corpo.
        </p>

        <p>
        Além dos sintomas, pessoas com fatores de risco como hipertensão, diabetes, colesterol alto, obesidade ou histórico familiar de doenças cardíacas devem realizar consultas regulares com um cardiologista, mesmo sem apresentar sinais aparentes.
        </p>

        <p>
        A prevenção é sempre o melhor caminho. Consultas periódicas, exames de rotina e hábitos saudáveis são fundamentais para manter o coração funcionando corretamente e evitar problemas mais sérios no futuro.
        </p>

        <p>
        Na Clínica Lima Estevam, valorizamos o cuidado preventivo e o acompanhamento individualizado de cada paciente. Contar com um cardiologista é essencial para manter sua saúde em dia e viver com mais tranquilidade e segurança. 💙
        </p>

